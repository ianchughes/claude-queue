def make_shortcut():
    r"""Create Desktop\Project Dashboard.lnk pointing at the installed pyw."""
    try:
        import subprocess
        target = TARGET / "Project Dashboard.pyw"
        lnk = HOME / "Desktop" / "Project Dashboard.lnk"
        ps = (
            f"$s=(New-Object -ComObject WScript.Shell)."
            f"CreateShortcut('{lnk}'); "
            f"$s.TargetPath='{target}'; "
            f"$s.WorkingDirectory='{TARGET}'; $s.Save()"
        )
        subprocess.run(
            ["powershell.exe", "-NoProfile", "-Command", ps],
            check=True, capture_output=True,
        )
        return lnk
    except Exception as e:
        print(f"  (skipped shortcut — {e})")
        return None


def main():
    print(f"Installing to: {TARGET}")
    TARGET.mkdir(parents=True, exist_ok=True)
    for name, b64_chunks in FILES.items():
        data = base64.b64decode("".join(b64_chunks))
        dest = TARGET / name
        dest.write_bytes(data)
        print(f"  wrote {name}  ({len(data)} bytes)")
    lnk = make_shortcut()
    if lnk:
        print(f"  created shortcut: {lnk}")
    print()
    print("Done. Double-click the Desktop shortcut — or the .pyw directly — to launch.")
    if sys.platform == "win32":
        try:
            input("Press Enter to close...")
        except EOFError:
            pass


if __name__ == "__main__":
    main()
